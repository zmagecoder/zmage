package com.mage.cms.common.model;

public class Widget {

	private String source_from;
	private Long widget_id;
	private String code;
	private String type;
	private String cms_type;
	private String visible;
	private String edit;
	private String move;
	private String widget_url;
	private String cache;
	private String state;
	private String create_time;
	public String getSource_from() {
		return source_from;
	}
	public void setSource_from(String source_from) {
		this.source_from = source_from;
	}
	public Long getWidget_id() {
		return widget_id;
	}
	public void setWidget_id(Long widget_id) {
		this.widget_id = widget_id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCms_type() {
		return cms_type;
	}
	public void setCms_type(String cms_type) {
		this.cms_type = cms_type;
	}
	public String getVisible() {
		return visible;
	}
	public void setVisible(String visible) {
		this.visible = visible;
	}
	public String getEdit() {
		return edit;
	}
	public void setEdit(String edit) {
		this.edit = edit;
	}
	public String getMove() {
		return move;
	}
	public void setMove(String move) {
		this.move = move;
	}
	public String getWidget_url() {
		return widget_url;
	}
	public void setWidget_url(String widget_url) {
		this.widget_url = widget_url;
	}
	public String getCache() {
		return cache;
	}
	public void setCache(String cache) {
		this.cache = cache;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

}
